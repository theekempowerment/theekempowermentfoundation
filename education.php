            <h2>The 8-4-4 System of Education</h2>
            <p>The 8-4-4 system is a Kenyan education structure that was introduced in 1985. It represents 8 years of primary education, 4 years of secondary education, and 4 years of university education. Under this system, students begin their formal education in Standard 1 and complete primary school after 8 years. They then proceed to secondary school for 4 years, where they sit for the Kenya Certificate of Secondary Education (KCSE) examination. After secondary school, students can choose to join a university, college, or pursue vocational training for another 4 years. The 8-4-4 system was designed to emphasize practical skills, self-reliance, and adaptability in a dynamic world.</p>
