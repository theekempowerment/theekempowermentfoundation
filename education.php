            <h2>Competency-Based Curriculum (CBC)</h2>
            <p>Kenya’s Competency-Based Curriculum (CBC) is a new education system designed to replace the 8-4-4 system. CBC focuses on developing learners’ competencies, including critical thinking, problem-solving, and creativity, rather than just the memorization of content. It emphasizes learner-centered education, ensuring students can apply their knowledge in real-life situations. The CBC is structured into a 2-6-3-3 system, where learners spend 2 years in pre-primary, 6 years in primary, 3 years in junior secondary, and 3 years in senior secondary. The curriculum aims to nurture every learner’s potential and talent, preparing them for the rapidly changing job market and life challenges.</p>
